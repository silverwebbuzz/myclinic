-- =====================================================================
-- ManageClinic — consolidated install SQL
-- Schema (migrations 001..011) + reference data (modules, drugs, remedies,
-- lab tests, platform admin).
--
-- Run as the database-scoped user (e.g. silverwebbuzz_in_myclinic).
-- Does NOT include CREATE DATABASE or USE — the connecting user is
-- expected to already be scoped to the target database.
--
-- Idempotent: drops tables first (FK-safe), then recreates everything.
-- Re-running this file fully resets the schema and reference data.
--
-- Demo clinic data (test patients/visits/invoices) is NOT included here.
-- For that, run: php database/seeds/demo_data.php
-- =====================================================================

